package cn.gyyx.pandora.model;

public class DptUsr {

    private Integer id;
    private String name;
    private Integer has_key;
    private Integer type;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getHas_key() {
        return has_key;
    }

    public void setHas_key(Integer has_key) {
        this.has_key = has_key;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }
}
